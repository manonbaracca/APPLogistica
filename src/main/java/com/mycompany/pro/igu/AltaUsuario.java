    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String usuario = txtUsuario.getText().trim();
        String contra = txtContra.getText().trim();
        String email = txtEmail.getText().trim();
        String tel = txtTel.getText().trim();
        String rol = (String) cmbRol.getSelectedItem();

        // Verificar que todos los campos est√©n llenos
        if (usuario.isEmpty() || contra.isEmpty() || email.isEmpty() || tel.isEmpty() || rol.isEmpty()) {
            mostrarMensaje("Todos los campos deben estar llenos.", "Error", "Error de Entrada");
            return;
        }
        if (usuario.length() < 1 || usuario.length() > 10) {
            mostrarMensaje("El usuario debe tener entre 1 y 10 caracteres.", "Error", "Error de Validaci√≥n");
            return;
        }
        if (contra.length() < 4 || contra.length() > 8) {
            mostrarMensaje("La contrase√±a debe tener entre 4 y 8 caracteres.", "Error", "Error de Validaci√≥n");
            return;
        }
        Usuario usuarioExistente = control.validarUsuario(usuario,contra);

        if (usuarioExistente != null) {
            mostrarMensaje("El nombre de usario ya est√° en uso en la base de datos. Ingrese un usuario √∫nico.", "Error", "Usuario Duplicado");
            return;
        }
        
    try {
        control.crearUsuario(usuario, contra, email, tel, rol);
    } catch (Exception ex) {
        Logger.getLogger(AltaUsuario.class.getName()).log(Level.SEVERE, null, ex);
    }
        this.dispose();
        mostrarMensaje("Usuario creado correctamente", "Info", "Creaci√≥n exitosa");
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

